package libraryofnotes.model;

public class Tag_old {
    private int id;
    private int tagname;

    public Tag_old(int id, int tagname) {
        this.id = id;
        this.tagname = tagname;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTagname() {
        return tagname;
    }

    public void setTagname(int tagname) {
        this.tagname = tagname;
    }
    
    
}
